public class ex3 {
    public static void main(String[] args) {
        for (int i = 100; i >= 1; i -= 1) { // Loop for que inicia i em 100, continua enquanto i for maior ou igual a 0, e decrementa i em 1 a cada iteração
            System.out.println(i + "\n"); // Imprime o valor atual de i seguido de uma nova linha
        }
    }
}