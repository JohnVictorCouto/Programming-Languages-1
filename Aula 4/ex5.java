public class ex5 {
    public static void main(String[] args) {
        int num = 20; // Inicializa a variável num com o valor 21
        for (int i = 1; i < num + 1; i++) {
            System.out.println(i + "\n"); // Imprime o valor de i seguido de uma nova linha
        }
        for (int i = num - 1; i >= 1; i--) {
            System.out.println(i + "\n"); // Imprime o valor de i seguido de uma nova linha
        }
    }
}