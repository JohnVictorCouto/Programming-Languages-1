public class ex2 {
    public static void main(String[] args) {
        int i = 100; // Inicializa a variável i com o valor 100
        do {
            System.out.println(i + "\n"); // Imprime o valor de i seguido de uma nova linha
            i -= 1; // Decrementa i em 1 a cada iteração do loop
        } while (i >= 1); // Continua o loop enquanto i for maior ou igual a 1
    }
}
