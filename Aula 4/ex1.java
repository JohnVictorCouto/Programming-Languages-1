public class ex1 {
    public static void main(String[] args) {
        int i = 100; // Inicializa a variável i com o valor 100
        while (i >= 1) { // Enquanto i for maior ou igual a 0, o loop continua
            System.out.println(i + "\n"); // Imprime o valor atual de i seguido de uma nova linha
            i -= 1; // Decrementa i em 1 a cada iteração do loop
        }
    }
}